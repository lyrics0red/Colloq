package org.example;

public class Character {
    public void MoveForward() {
        System.out.println("Персонаж идет впедёр\n");
    }
    public void MoveBack() {
        System.out.println("Персонаж идет назад\n");
    }
    public void MoveRight() {
        System.out.println("Персонаж идет вправо\n");
    }
    public void MoveLeft() {
        System.out.println("Персонаж идет влево\n");
    }
}
